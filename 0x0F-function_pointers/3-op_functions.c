#include "3-calc.h"

/**
 * op_add - Sum two numbers
 *
 * @a: number
 * @b: number
 *
 * Return: Always 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Sub two numbers
 *
 * @a: number
 * @b: number
 *
 * Return: Always 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Mult two numbers
 *
 * @a: number
 * @b: number
 *
 * Return: Always 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Div two numbers
 *
 * @a: number
 * @b: number
 *
 * Return: Always 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Mod two numbers
 *
 * @a: number
 * @b: number
 *
 * Return: Always 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
